// Utility functions for accessing theme values
export const getColor = (color: ThemeColors, shade?: keyof typeof theme.colors[ThemeColors]) => {
  if (shade && typeof theme.colors[color] === 'object' && shade in theme.colors[color]) {
    return (theme.colors[color] as any)[shade];
  }
  return theme.colors[color];
};

export const getTypography = (type: ThemeTypography, property?: keyof typeof theme.typography[ThemeTypography]) => {
  if (property && typeof theme.typography[type] === 'object' && property in theme.typography[type]) {
    return (theme.typography[type] as any)[property];
  }
  return theme.typography[type];
};

